n = int(input())

# dp[i][j]: i번째 행까지 고려했을 때, i번째 행의 상태가 j인 경우의 수
# j=0: 배치 안 함, j=1: 왼쪽만, j=2: 오른쪽만
dp = [[0] * 3 for _ in range(n + 1)]

# 초기값
dp[1][0] = 1  # 1행에 사자를 배치하지 않음
dp[1][1] = 1  # 1행 왼쪽에만 배치
dp[1][2] = 1  # 1행 오른쪽에만 배치

# 점화식 적용
for i in range(2, n + 1):
    # 현재 행에 배치하지 않는 경우: 이전 행의 모든 경우 가능
    dp[i][0] = (dp[i-1][0] + dp[i-1][1] + dp[i-1][2]) % 9901
    
    # 현재 행 왼쪽에만 배치: 이전 행이 비었거나 오른쪽만 있는 경우
    dp[i][1] = (dp[i-1][0] + dp[i-1][2]) % 9901
    
    # 현재 행 오른쪽에만 배치: 이전 행이 비었거나 왼쪽만 있는 경우
    dp[i][2] = (dp[i-1][0] + dp[i-1][1]) % 9901

# 마지막 행의 모든 경우의 수를 합산
answer = (dp[n][0] + dp[n][1] + dp[n][2]) % 9901
print(answer)