N = int(input())

# dp[i][j]: i자리 이친수 중 마지막 자리가 j인 경우의 수
dp = [[0] * 2 for _ in range(N + 1)]

# 초기값: 1자리 이친수는 "1"만 가능
dp[1][1] = 1

# 점화식 적용
for i in range(2, N + 1):
    dp[i][0] = dp[i-1][0] + dp[i-1][1]  # 마지막이 0: 이전이 0이든 1이든 가능
    dp[i][1] = dp[i-1][0]                # 마지막이 1: 이전이 0이어야 함

# N자리 이친수의 총 개수
print(dp[N][0] + dp[N][1])